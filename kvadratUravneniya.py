print ('Введите переменные')
a = int(input())
b = int(input())
c = int(input())
D = b ** 2 - 4 * a * c
d = D ** 0.5
if D < 0:
 print ('Корней уравнения не существуют')
if D == 0:
 x = (-1 * b) / 2 * a
 print ('Корень уравнения', x)
if D > 0:
 z = (-1 * b - d) / (2 * a)
 y = (-1 * b + d) / (2 * a)
 print ('Наименьший корень равен', z)
 print ('Наибольший корень равен', y)
